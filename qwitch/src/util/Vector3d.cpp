#include "Vector3d.hpp"

namespace qwitch {

//---------------------------------------------------------------------
// 
//  
// 
//
Vector3d::Vector3d()
    : mX(0.0)
    , mY(0.0)
    , mZ(0.0)
{
}

//---------------------------------------------------------------------
// 
//  
// 
//
Vector3d::Vector3d(double aX, double aY, double aZ)
    : mX(aX)
    , mY(aY)
    , mZ(aZ)
{
}

//---------------------------------------------------------------------
// 
//  
// 
//
Vector3d& Vector3d::operator+=(const Vector3d& aRhs)
{
    mX += aRhs.x();
    mY += aRhs.y();
    mZ += aRhs.z();
    return *this;
}

//---------------------------------------------------------------------
// 
//  
// 
//
void Vector3d::move(double aX, double aY, double aZ)
{
    mX += aX;
    mY += aY;
    mZ += aZ;
}

//---------------------------------------------------------------------
// 
//  
// 
//
void Vector3d::setX(double aX)
{
    mX = aX;
}
//---------------------------------------------------------------------
void Vector3d::setY(double aY)
{
    mY = aY;
}
//---------------------------------------------------------------------
void Vector3d::setZ(double aZ)
{
    mZ = aZ;
}

//---------------------------------------------------------------------
// 
//  
// 
//
double Vector3d::x() const
{
    return mX;
}
//---------------------------------------------------------------------
double Vector3d::y() const
{
    return mY;
}
//---------------------------------------------------------------------
double Vector3d::z() const
{
    return mZ;
}

} // namespace